// ===== IPv6 地址解析 =====
// 导出
export { lookupProvince, PREFIX_MAP };
/**
 * 将 IPv6 地址展开为完整 8 组 4 位 hex 格式
 * 例：240e:47c::1 → 240e:047c:0000:0000:0000:0000:0000:0001
 */
function expandIPv6(ip) {
  // 处理 :: 缩写
  if (ip.includes("::")) {
    const parts = ip.split("::");
    const left = parts[0] ? parts[0].split(":") : [];
    const right = parts[1] ? parts[1].split(":") : [];
    const missing = 8 - left.length - right.length;
    const expanded = [...left, ...Array(missing).fill("0"), ...right];
    ip = expanded.join(":");
  }
  // 每组补零到 4 位，转小写
  return ip.split(":").map(g => g.padStart(4, "0").toLowerCase()).join(":");
}
/**
 * 提取 IPv6 地址的 /32 前缀（前 8 位 hex 字符串）
 * 例：240e:047c:86e8:346:... → "240e047c"
 */
function extractPrefix32(ip) {
  try {
    const full = expandIPv6(ip);
    // 前2组 = 前32位
    const groups = full.split(":");
    return groups[0] + groups[1];
  } catch (e) {
    return null;
  }
}
/**
 * 根据 IPv6 地址查找省份
 * 返回省份中文名，未找到返回 null
 */
function lookupProvince(ip) {
  if (!ip || !ip.includes(":")) return null; // 不是 IPv6
  
  const prefix = extractPrefix32(ip);
  if (!prefix) return null;
  return PREFIX_MAP[prefix] || null;
}
